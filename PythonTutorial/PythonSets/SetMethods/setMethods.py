#                                 >           Returns whether all items in other, specified set(s) are present in this set
# pop()                                       Removes an element from the set
# remove()                                    Remove the specified element
# symmetric_difference()          ^           Returns a set with the symmetric difference of two sets
# symmetric_difference_update()   ^=          Inserts the symmetric differences from this set and another
# union()                         |           Return a set containing the union of sets
# update()                        |=          Uodate the set with the union of this set and others