# What is OOP?

# OOP stands for Object-Oriented Programming
# In OOP, you create objects.
# Each object can store data and have functions that 
# work with that data

# Advantages of OOP

#   Provides a clear structure to programs
#   Makes code easier to maintain, reuse, and debug
#   Helps keep you code DRY (Don't Repeat Yourself)
#   Allows you to build reusable applications with less code

# What are Classes and Objects?
# A class defines what an object should look like, and 
# an object is created on said class.

